def write_locations_to_csv(locations):
    with open('locations.csv', 'w', newline='') as csvfile:
        writer = csv.writer(csvfile)
        writer.writerow(['ID', 'Name', 'Latitude', 'Longitude'])
        for location_id, location in locations.items():
            writer.writerow([location_id, location['name'], location['coords'][0], location['coords'][1]])

def write_connections_to_csv(connections):
    with open('connections.csv', 'w', newline='') as csvfile:
        writer = csv.writer(csvfile)
        writer.writerow(['From', 'To', 'Distance'])
        for connection in connections:
            writer.writerow([connection[0], connection[1], connection[2]])


